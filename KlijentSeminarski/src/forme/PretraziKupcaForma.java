    private void dizajn() {
        // Fontovi
        Font labelFont = new Font("Times New Roman", Font.BOLD, 18);
        Font textFont = new Font("Times New Roman", Font.PLAIN, 16);
        Font dugmeFont = new Font("Times New Roman", Font.BOLD, 16);
        Font checkFont = new Font("Times New Roman", Font.PLAIN, 16);

        // Pozadina forme
        getContentPane().setBackground(new java.awt.Color(245, 245, 245)); // svetlo siva
        setSize(800, 400);
        setResizable(false);
        setLocationRelativeTo(null);

        // Labele
        JLabel[] labele = { jLabel1, jLabel2, jLabel3 };
        java.awt.Color labelColor = new java.awt.Color(46, 78, 31); // tamna zelena
        for (JLabel lbl : labele) {
            lbl.setFont(labelFont);
            lbl.setForeground(labelColor);
        }

        // Tekstualna polja
        JTextField[] polja = { jTextFieldImeIPrezime };
        java.awt.Color inputTextColor = new java.awt.Color(43, 43, 43);
        java.awt.Color inputBg = Color.WHITE;
        java.awt.Color disabledBg = new java.awt.Color(220, 220, 220);

        for (JTextField tf : polja) {
            tf.setFont(textFont);
            tf.setBorder(BorderFactory.createLineBorder(new java.awt.Color(46, 78, 31), 2));
            if(tf.isEnabled()) {
                tf.setBackground(inputBg);
                tf.setForeground(inputTextColor);
            } else {
                tf.setBackground(disabledBg);
                tf.setForeground(Color.DARK_GRAY);
            }
        }

        // Dugmad
        JButton[] buttons = { jButtonPretrazi, jButtonPrikaziKupca, jButtonPrikaziSveKupce };
        java.awt.Color dugmeEnabledBg = new java.awt.Color(46, 78, 31);
        java.awt.Color dugmeDisabledBg = new java.awt.Color(180, 180, 180);
        java.awt.Color dugmeEnabledFg = Color.WHITE;
        java.awt.Color dugmeDisabledFg = new java.awt.Color(80, 80, 80);

        for (JButton btn : buttons) {
            btn.setFont(dugmeFont);
            btn.setFocusPainted(false);
            btn.setBorderPainted(false);

            if(btn.isEnabled()) {
                btn.setBackground(dugmeEnabledBg);
                btn.setForeground(dugmeEnabledFg);
            } else {
                btn.setBackground(dugmeDisabledBg);
                btn.setForeground(dugmeDisabledFg);
            }
        }

        // Checkboxovi
        JCheckBox[] checkovi = { jCheckBoxImeIPrezime, jCheckBoxMesto };
        for (JCheckBox cb : checkovi) {
            cb.setFont(checkFont);
            cb.setBackground(getContentPane().getBackground());
            cb.setForeground(labelColor);
        }

        // ComboBox
        jComboBoxMesto.setFont(textFont);
        jComboBoxMesto.setBackground(Color.WHITE);
        jComboBoxMesto.setForeground(inputTextColor);

        // Tabela
        jTableKupac.setFont(textFont);
        jTableKupac.setBackground(Color.WHITE);
        jTableKupac.setForeground(inputTextColor);
        jTableKupac.getTableHeader().setFont(labelFont);
    }

    
    
    


