    private void dizajn() {
    // Fontovi
        Font labelFont = new Font("Times New Roman", Font.BOLD, 18);
        Font textFont = new Font("Times New Roman", Font.BOLD, 18);
        Font dugmeFont = new Font("Times New Roman", Font.BOLD, 18);
        Font comboFont = new Font("Times New Roman", Font.PLAIN, 16);

        // Pozadina forme
        getContentPane().setBackground(new java.awt.Color(245, 245, 245)); // svetlo siva
        setSize(950, 500);
        setResizable(false);
        setLocationRelativeTo(null);

        // Pozadina panela
        jPanel1.setBackground(new java.awt.Color(220, 210, 200)); 
        jPanel1.setSize(300, 250);// toplija nijansa

        // Labele
        JLabel[] labele = {
            jLabel1, jLabel2, jLabel3, jLabel4, jLabel5,
            jLabel6, jLabel7, jLabel8, jLabel9, jLabelId
        };
        java.awt.Color labelColor = new java.awt.Color(46, 78, 31); // tamna zelena
        for (JLabel lbl : labele) {
            lbl.setFont(labelFont);
            lbl.setForeground(labelColor);
        }

        // Tekstualna polja
        JTextField[] polja = {
            jTextFieldDatum1, jTextFieldId, jTextFieldIznosRacuna, jTextFieldKolicina
        };
        java.awt.Color inputTextColor = new java.awt.Color(43, 43, 43);
        java.awt.Color inputBg = Color.WHITE;
        java.awt.Color disabledBg = new java.awt.Color(220, 220, 220);
        Color inputDisabledBg = new Color(200, 230, 200);

        for (JTextField tf : polja) {
            tf.setFont(textFont);
            tf.setBorder(BorderFactory.createLineBorder(new java.awt.Color(46, 78, 31), 2));
          
        }

        // ComboBox
        JComboBox<?>[] combos = { jComboBoxKnjiga, jComboBoxKupac, jComboBoxProdavac };
        for (JComboBox<?> combo : combos) {
            combo.setFont(comboFont);
            if(combo.isEnabled()) {
                combo.setBackground(Color.WHITE);
                combo.setForeground(inputTextColor);
            } else {
                combo.setBackground(disabledBg);
                combo.setForeground(Color.DARK_GRAY);
            }
        }

        // Dugmad
        JButton[] buttons = {
            jButtonDodajKnjigu, jButtonKreiraj, jButtonOdustani1, jButtonOdustaniRacun,
            jButtonPrikaziStavku, jButtonPromeni, jButtonPromeniRacunDugme,
            jButtonSacuvajPromeneKnjiga
        };
        java.awt.Color dugmeEnabledBg = new java.awt.Color(46, 78, 31);
        java.awt.Color dugmeDisabledBg = new java.awt.Color(180, 180, 180);
        java.awt.Color dugmeEnabledFg = Color.WHITE;
        java.awt.Color dugmeDisabledFg = new java.awt.Color(80, 80, 80);

        for (JButton btn : buttons) {
            btn.setFont(dugmeFont);
            btn.setFocusPainted(false);
            btn.setBorderPainted(false);

            if(btn.isEnabled()) {
                btn.setBackground(dugmeEnabledBg);
                btn.setForeground(dugmeEnabledFg);
            } else {
                btn.setBackground(dugmeDisabledBg);
                btn.setForeground(dugmeDisabledFg);
            }
        }

        // Tabela
        jTableStavke.setFont(textFont);
        jTableStavke.setBackground(Color.WHITE);
        jTableStavke.setForeground(inputTextColor);
        jTableStavke.getTableHeader().setFont(labelFont);
    }




